    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String datoTemp = jTextField1.getText().trim();
        String path = jTextField2.getText().trim();
        estatus = "";
        
        if(!datoTemp.isEmpty()){
            char dato = datoTemp.charAt(0);
            if(ArbolInterface.Insertar(dato, path)){
                estatus = "se inserto correctamente el valor "+ dato;
                jTextField1.setText("");
            }else{
              estatus = "error al insertar el dato";  
            }
        }
        jLabel4.setText(estatus);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     String path = jTextField2.getText().trim();
     estatus = "";
        if(!path.isEmpty()){
            if(ArbolInterface.Eliminar(path)){
                estatus = "se elimino el dato correctamente";
                  jTextField1.setText("");
                  jTextField2.setText("");
            }else{
                estatus = "ERROR al eliminar";
            }
        }
 
        jLabel4.setText(estatus);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        estatus = "";
        if (ArbolInterface.BuscarPath(jTextField2.getText().trim())!= null) {
            estatus = "Si existe el nodo";
        }else{
            estatus = "No se encontro el nodo";
        }
        jLabel4.setText(estatus);
    }//GEN-LAST:event_jButton3ActionPerformed

